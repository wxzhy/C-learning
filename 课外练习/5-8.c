#include <stdio.h>

int main() {
	long long l;
	double m;
	scanf("%lld",&l);
	if(l<=100000)
		m=l*0.1;
	else {
		m=100000*0.1;
		if(l>100000&&l<=200000)
			m+=(l-100000)*0.075;
		else {
			m+=100000*0.075;
			if(l>200000&&l<=400000)
				m+=(l-200000)*0.05;
			else {
				m+=200000*0.05;
				if(l>400000&&l<=600000)
					m+=(l-400000)*0.03;
				else {
					m+=200000*0.03;
					if(l>600000&&l<=1000000)
						m+=(l-600000)*0.015;
					else {
						m+=400000*0.015;
						if(l>1000000)
							m+=(l-1000000)*0.01;
					}
				}
			}
		}
	}
	printf("%.0lf",m);
	return 0;
}